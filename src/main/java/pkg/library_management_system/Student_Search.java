    private void loadStudentProfiles(){
        Connection cnt= null;
        Statement stmt = null;
        ResultSet rs = null;
        try {
            cnt= jdbc_test.getCon();
            stmt = cnt.createStatement();
            String query = "SELECT * FROM student_profile";
            rs = stmt.executeQuery(query);

            // Clear the existing data in the table model
            tableModel.setRowCount(0);

            // Populate the table model with the data from the result set
            while (rs.next()) {
                Vector<Object> row = new Vector<>();
                row.add(rs.getString("Std_ID"));
                row.add(rs.getString("Fname"));
                row.add(rs.getString("Lname"));
                row.add(rs.getString("Most_Recent_Book_Title"));
                row.add(rs.getString("First_Borrow_Date"));
                row.add(rs.getString("Last_Return_date"));
                row.add(rs.getString("Overdue_Status"));
                row.add(rs.getString("nbr_of_borrowings_per_month"));
                row.add(rs.getString("nbr_of_borrowings_per_year"));
                row.add(rs.getString("nbr_of_overdue_books"));
                row.add(rs.getString("avg_borrow_duration"));


                tableModel.addRow(row);        
            }
        }
        
        catch (SQLException e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }

