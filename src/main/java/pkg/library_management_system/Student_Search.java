        try {
        while (rs != null && rs.next()) {
            Object[] row = {
                rs.getString("Std_ID"),
                rs.getString("Fname"),
                rs.getString("Lname"),
                rs.getString("Most_Recent_Book_Title"),
                rs.getString("First_Borrow_Date"),
                rs.getString("Last_Return_date"),
                rs.getString("Overdue_Status"),
                rs.getString("nbr_of_borrowings_per_month"),
                rs.getString("nbr_of_borrowings_per_year"),
                rs.getString("nbr_of_overdue_books"),
                rs.getObject("avg_borrow_duration")

            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
