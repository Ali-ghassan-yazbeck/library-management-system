    private void loadStudentData(){
        Connection cnt= null;
        Statement stmt = null;
        ResultSet rs = null;
        try {
            cnt= jdbc_test.getCon();
            stmt = cnt.createStatement();
            String query = "SELECT * FROM student";
            rs = stmt.executeQuery(query);

            // Clear the existing data in the table model
            tableModel.setRowCount(0);

            // Populate the table model with the data from the result set
            while (rs.next()) {
                Vector<Object> row = new Vector<>();
                row.add(rs.getString("std_ID"));
                row.add(rs.getString("Fname"));
                row.add(rs.getString("MidName"));
                row.add(rs.getString("Lname"));
                row.add(rs.getString("Email"));
                row.add(rs.getString("Phone"));
                row.add(rs.getString("Faculty"));

                tableModel.addRow(row);        
            }
        }
        
        catch (SQLException e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
    
    
