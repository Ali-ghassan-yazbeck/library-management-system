     private boolean validateInputs(){
         
         boolean result = false;
         if (tf_stdID.getText().trim().isEmpty() || tf_email.getText().trim().isEmpty() ||
        tf_Fname.getText().trim().isEmpty() || tf_midName.getText().trim().isEmpty() ||
        tf_Lname.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill in all fields", "Error", JOptionPane.ERROR_MESSAGE);

        }
         else  if (!isValidStudentID(tf_stdID.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a valid Student ID of 5 digits.", "Invalid input", JOptionPane.ERROR_MESSAGE);

         }
         else if (!isValidFirstLastName(tf_Fname.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a valid First Name.", "Invalid input", JOptionPane.ERROR_MESSAGE);

         }
         else if (!isValidMiddleName(tf_midName.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a single letter for the Middle Name.", "Invalid input", JOptionPane.ERROR_MESSAGE);

         }
         else if (!isValidFirstLastName(tf_Lname.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a valid Last Name.", "Invalid input", JOptionPane.ERROR_MESSAGE);

         }
         else if (!isValidPhone(tf_phone.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a valid phone number.", "Invalid input", JOptionPane.ERROR_MESSAGE);

         }
         else if (!isValidEmail(tf_email.getText())){
            JOptionPane.showMessageDialog(null, "Please enter a valid gmail or hotmail address.", "Invalid input", JOptionPane.ERROR_MESSAGE);

         }
         
         else
             result =true;
         
         return result;
         
     }
     
     
     public boolean isValidStudentID(String studentID) {
    // Regex pattern to ensure the ID is exactly 5 digits long and contains only numbers
    String idRegex = "^[0-9]{5}$";
    Pattern pattern = Pattern.compile(idRegex);
    Matcher matcher = pattern.matcher(studentID);
    return matcher.matches();
}
     public boolean isValidMiddleName(String middleName) {
    // Regex pattern to ensure the middle name is exactly one letter (uppercase or lowercase)
    String middleNameRegex = "^[a-zA-Z]$";
    Pattern pattern = Pattern.compile(middleNameRegex);
    Matcher matcher = pattern.matcher(middleName);
    return matcher.matches();
}
     public boolean isValidFirstLastName(String name) {
    // Regex pattern to ensure the first name or last name only contains letters
    String nameRegex = "^[a-zA-Z]+$"; 
    Pattern pattern = Pattern.compile(nameRegex);
    Matcher matcher = pattern.matcher(name);
    return matcher.matches();
}
     
     public boolean isValidPhone( String phone){
    // Regex pattern to ensure the middle name is exactly one letter (uppercase or lowercase)
    String phoneRegex =  "^[0-9]{8}$";
    Pattern pattern = Pattern.compile(phoneRegex);
    Matcher matcher = pattern.matcher(phone);
    return matcher.matches();         
     }
     
    public boolean isValidEmail(String email) {
    // Regex pattern for emails ending in @gmail.com or @hotmail.com
    String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(gmail|hotmail)\\.com$";
    // Compile the regex
    Pattern pattern = Pattern.compile(emailRegex);
    // Match the email string with the regex pattern
    Matcher matcher = pattern.matcher(email);
    // Return whether it matches the pattern
    return matcher.matches();
          }



     
