    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
        // TODO add your handling code here:
         if (validateInputs()){
             
                // Insert into the database
                String stdID = tf_stdID.getText();
                String Fname = tf_Fname.getText();
                String MidName = tf_midName.getText();
                String Lname = tf_Lname.getText();
                String email = tf_email.getText();
                String phone = tf_phone.getText();
                String faculty = cb_Faculty.getSelectedItem().toString();
                
                Student s = new Student (stdID, Fname, MidName, Lname, email , phone, faculty);
                s.addStudent();
               
                loadStudentData();
                
                // Clear the text fields and selected item
                tf_stdID.setText("");
                tf_email.setText("");
                tf_Fname.setText("");
                tf_midName.setText("");
                tf_Lname.setText("");
                tf_phone.setText(" ");
                cb_Faculty.setSelectedIndex(-1);
            }
         
         
        
           

    }//GEN-LAST:event_btn_AddActionPerformed

